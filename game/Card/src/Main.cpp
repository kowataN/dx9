void	MainLoop( void )
{
	switch( g_MainMode )
	{
		//	メイン初期化
		case	MainModeInit:
			MainInit();
			g_MainMode = MainModeTitle;

		//	タイトル
		case	MainModeTitle:
			g_MainMode = MainModeMain;
			break;

		//	ゲームメイン
		case	MainModeMain:
			GameLoop();
			break;

		case	MainModeEnd:
			break;
	}

	LoopAnm();
}

