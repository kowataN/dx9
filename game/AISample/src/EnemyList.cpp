/******************************************************************************/
//	コンストラクタ
//		戻り値：
//				なし
//		引数：
//				なし
/******************************************************************************/
CEnemyList::CEnemyList()
{
	memset( &m_Use, 0x00, sizeof(m_Use) );
}

/******************************************************************************/
//	デストラクタ
//		戻り値：
//				なし
//		引数：
//				なし
/******************************************************************************/
CEnemyList::~CEnemyList()
{
}

/******************************************************************************/
//	エネミー初期化
//		戻り値：
//				なし
//		引数：
//				なし
/******************************************************************************/
void	CEnemyList::Init( long EnemyNo, long ObjNo, long TargetNo, double PosX, double PosY )
{
	m_List[ EnemyNo ].Init( ObjNo, TargetNo, PosX, PosY );
	m_Use[ EnemyNo ] = ON;
}

/******************************************************************************/
//	エネミーメインループ
//		戻り値：
//				なし
//		引数：
//				なし
/******************************************************************************/
void	CEnemyList::MainLoop( void )
{
	for( int i=0; i<ENEMY_MAX; i++ )
	{
		if( m_Use[ i ] )
		{
			m_List[ i ].MainLoop();
		}
	}
}
